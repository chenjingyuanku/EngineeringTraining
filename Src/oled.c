void OLED_ClearScreen(void)
{
    uint8_t x = 0,y = 0;
    for(y = 0;y < 8;y++)
    {
        for(x = 0;x < 128;x++)
        {
            OLED_GRAM[x][y] = 0x00;
        }
    }
    //OLED_Updata();
}

/*OLED屏幕更新函数*/
void OLED_Updata(void)
{
    uint8_t x = 0,y = 0;
    for(y = 0;y < 8;y++)
    {
        OLED_Set_Pos(0,y);
        for(x = 0;x <128;x++)
        {
            OLED_WR_Byte(OLED_GRAM[x][y],OLED_DATA);
        }
    }
}

void OLED_UpdataPart(uint8_t row1,uint8_t col1,uint8_t row2,uint8_t col2,uint8_t direct)
{
    uint8_t x = 0,y = 0;
    if(direct == 1)
    {
        OLED_WR_Byte(0x20,0);
        OLED_WR_Byte(0x00,0);
        OLED_WR_Byte(0x21,0);
        OLED_WR_Byte(col1,0);
        OLED_WR_Byte(col2,0);
        OLED_WR_Byte(0x22,0);
        OLED_WR_Byte(row1,0);
        OLED_WR_Byte(row2,0);
        for(y = row1;y<=row2;y++)
        {
            for(x = col1;x<=col2;x++)
            {
                OLED_WR_Byte(OLED_GRAM[x][y],1);
            }
        }
    }
    else
    {
        OLED_WR_Byte(0x20,0);
        OLED_WR_Byte(0x01,0);
        OLED_WR_Byte(0x21,0);
        OLED_WR_Byte(col1,0);
        OLED_WR_Byte(col2,0);
        OLED_WR_Byte(0x22,0);
        OLED_WR_Byte(row1,0);
        OLED_WR_Byte(row2,0);
        for(y = col1;y<=col2;y++)
        {
            for(x = row1;x<=row2;x++)
            {
                OLED_WR_Byte(OLED_GRAM[x][y],1);
            }
        }
    }
    OLED_WR_Byte(0x20,0);
    OLED_WR_Byte(0x02,0);
}

























