//清屏函数,清完屏,整个屏幕是黑色的!和没点亮一样!!!      
void OLED_Clear(void)  
{  
    uint8_t i,n;            
    for(i=0;i<8;i++)  
    {  
        OLED_WR_Byte (0xb0+i,OLED_CMD);    //设置页地址（0~7）
        OLED_WR_Byte (0x02,OLED_CMD);      //设置显示位置―列低地址
        OLED_WR_Byte (0x10,OLED_CMD);      //设置显示位置―列高地址   
        for(n=0;n<128;n++)OLED_WR_Byte(0,OLED_DATA); 
    } //更新显示
}


//在指定位置显示一个字符,包括部分字符
//x:0~127
//y:0~63
//mode:0,反白显示;1,正常显示                 
//size:选择字体 16/12 
void OLED_ShowChar(uint8_t x,uint8_t y,uint8_t chr)
{          
    uint8_t c=0,i=0;    
    c=chr-' ';//得到偏移后的值            
    if(x>Max_Column-1)
    {
        x=0;
        y=y+2;
    }
    if(SIZE ==16)
    {
        OLED_Set_Pos(x,y);    
        for(i=0;i<8;i++)
        OLED_WR_Byte(F8X16[c*16+i],OLED_DATA);
        OLED_Set_Pos(x,y+1);
        for(i=0;i<8;i++)
        OLED_WR_Byte(F8X16[c*16+i+8],OLED_DATA);
    }
    else
    {    
        OLED_Set_Pos(x,y);  //12号字体 6*8 这里的y不用+1
        for(i=0;i<6;i++)
        {
            OLED_WR_Byte(F6x8[c][i],OLED_DATA);
        }
    }
}

/*OLED字符显示函数*/
void OLED_DisplayChar(uint8_t x,uint8_t y,uint8_t chr)
{
    static int8_t col = 0;
    if(SIZE == 12)
    {
        for(col = 0;col < 6;col++)
        {
            x = x > 20?20:x;
            OLED_GRAM[x*6+col][y] |= F6x8[chr - 0x20][col];
        }
    }
}

//m^n函数
uint32_t oled_pow(uint8_t m,uint8_t n)
{
    uint32_t result=1;     
    while(n--)result*=m;    
    return result;
}                  
//显示2个数字
//x,y :起点坐标     
//len :数字的位数
//size:字体大小
//mode:模式    0,填充模式;1,叠加模式
//num:数值(0~4294967295);               
void OLED_ShowNum(uint8_t x,uint8_t y,uint32_t num,uint8_t len,uint8_t size)
{             
    uint8_t t,temp;
    uint8_t enshow=0;                           
    for(t=0;t<len;t++)
    {
        temp=(num/oled_pow(10,len-t-1))%10;
        if(enshow==0&&t<(len-1))
        {
            if(temp==0)
            {
                OLED_ShowChar(x+(size/2)*t,y,' ');
                continue;
            }else enshow=1; 
              
        }
         OLED_DisplayChar(x+(size/2)*t,y,temp+'0'); 
    }
} 
//显示一个字符号串
void OLED_ShowString(uint8_t x,uint8_t y,uint8_t *chr)
{
    uint8_t j=0;
    while (chr[j]!='\0')
    {    
        OLED_DisplayChar(x,y,chr[j]);
        if(SIZE == 12)
        {
            //调用OLED_DisplayChar这个x+1即可
            x+=1;
            //调用OLED_ShowChar这个x+=6
            //x+=6;//一行显示21个数字
        }
        else if(SIZE == 16)
        {
            x+=8;
        }
        if(x>120)
        {
            x=0;
            y+=2;
        }
        j++;
    }
}
